package classbean;

/**
 *
 * @author Mamin
 */
public class Vehicle {
    private String driver;
    private String mileage;
    private String model;
    private String platNumber;
    private int serviceMileage;
    private String status;
    private String type;
    private int year;

    /**
     * @return the driver
     */
    public String getDriver() {
        return driver;
    }

    /**
     * @param driver the driver to set
     */
    public void setDriver(String driver) {
        this.driver = driver;
    }

    /**
     * @return the mileage
     */
    public String getMileage() {
        return mileage;
    }

    /**
     * @param mileage the mileage to set
     */
    public void setMileage(String mileage) {
        this.mileage = mileage;
    }

    /**
     * @return the model
     */
    public String getModel() {
        return model;
    }

    /**
     * @param model the model to set
     */
    public void setModel(String model) {
        this.model = model;
    }

    /**
     * @return the platNumber
     */
    public String getPlatNumber() {
        return platNumber;
    }

    /**
     * @param platNumber the platNumber to set
     */
    public void setPlatNumber(String platNumber) {
        this.platNumber = platNumber;
    }

    /**
     * @return the serviceMileage
     */
    public int getServiceMileage() {
        return serviceMileage;
    }

    /**
     * @param serviceMileage the serviceMileage to set
     */
    public void setServiceMileage(int serviceMileage) {
        this.serviceMileage = serviceMileage;
    }

    /**
     * @return the status
     */
    public String getStatus() {
        return status;
    }

    /**
     * @param status the status to set
     */
    public void setStatus(String status) {
        this.status = status;
    }

    /**
     * @return the type
     */
    public String getType() {
        return type;
    }

    /**
     * @param type the type to set
     */
    public void setType(String type) {
        this.type = type;
    }

    /**
     * @return the year
     */
    public int getYear() {
        return year;
    }

    /**
     * @param year the year to set
     */
    public void setYear(int year) {
        this.year = year;
    }
    
    
}
